"""Image preprocessing.

Images are the simplest media type — they are sent directly to the model
(as a file path, data URI, or URL). Thumbnails are generated by resizing
with Pillow.
"""

from __future__ import annotations

import logging
from pathlib import Path
from typing import Any

from ai_sandbox.media.base import MediaProcessor, _image_to_data_uri

LOGGER = logging.getLogger(__name__)

IMAGE_EXTENSIONS = frozenset({".png", ".jpg", ".jpeg", ".webp", ".gif", ".bmp", ".tiff"})


class ImageProcessor(MediaProcessor):
    """Classify, preview, and prepare image files."""

    extensions = IMAGE_EXTENSIONS

    @classmethod
    def is_image(cls, path: Path) -> bool:
        """Return True if the file extension indicates an image."""
        return cls.matches(path)

    @classmethod
    def make_thumbnail_data_uri(cls, path: Path, max_size: int = 80) -> str | None:
        """Resize the image and return a small base64 PNG data URI."""
        try:
            from PIL import Image as _PILImage
            img = _PILImage.open(path)
            return _image_to_data_uri(img, max_size)
        except Exception:
            LOGGER.debug("Failed to create thumbnail for %s", path, exc_info=True)
            return None

    @classmethod
    def preprocess(cls, path: Path, **kwargs: Any) -> dict[str, Any]:
        """Return the image path — no transformation needed.

        Returns ``{"image_paths": [path]}``.
        """
        return {"image_paths": [path]}
